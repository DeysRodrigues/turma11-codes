# EXERCÍCIO 01

user_age = int(input("Quantos anos você tem? "))
# condicional que coisa tal coisa
if user_age <- 0:
  print("Você não é nem projeto ainda.")
elif user_age <= 12:
  print("Criança")
elif user_age <= 17:
  print("Adolescente")
elif user_age <= 64:
  print("Adulto")
else:
  print("Idoso")

